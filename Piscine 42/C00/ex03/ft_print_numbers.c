#include <unistd.h>

void	ft_print_numbers(void);

void	ft_print_numbers(void)
{
	int	a;
	int	b;

	b = '9';
	a = '0';
	while (a <= b)
	{
		write (1, &a, 1);
		a++;
	}
}
